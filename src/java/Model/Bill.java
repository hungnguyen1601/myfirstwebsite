package Model;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Bill {
    private int id,customerId,status;
    private Date date;
    private double total;
    
    public Bill() {
        
    }

    public Bill(int id, Date date, double total) {
        this.id = id;
        this.date = date;
        this.total = total;
    }

    public Bill(int id, Date date, double total,int status) {
        this.id = id;
        this.status = status;
        this.date = date;
        this.total = total;
    }

    public int getCustomerId() {
        return customerId;
    }

    public void setCustomerId(int customerId) {
        this.customerId = customerId;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
    
    
}
